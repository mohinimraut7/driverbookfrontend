import React, { useState, useEffect } from "react";
import { Phone, Mail, MapPin } from "lucide-react";

const CompanyContactUs = () => {
  const [formData, setFormData] = useState({
    name: "",
    email: "",
    mobilenumber: "",
    message: "",
  });

  const [loading, setLoading] = useState(false);
  const [success, setSuccess] = useState("");
  const [error, setError] = useState("");

  // Check token on mount
  useEffect(() => {
    const token = localStorage.getItem("token");
    if (!token) {
      setError("Please log in first to send a message.");
    }
  }, []);

  const handleChange = (e) => {
    const { name, value } = e.target;
    setFormData((prev) => ({ ...prev, [name]: value }));
  };

  const handleSubmit = async (e) => {
    e.preventDefault();
    setLoading(true);
    setSuccess("");
    setError("");

    const token = localStorage.getItem("token");
    if (!token) {
      setError("No token found. Please log in again.");
      setLoading(false);
      return;
    }

    try {
      const response = await fetch("http://localhost:5000/api/contact", {
        method: "POST",
        headers: {
          "Content-Type": "application/json",
          Authorization: `Bearer ${token}`,
        },
        body: JSON.stringify({
          ...formData,
          user: null, // As per Postman
        }),
      });

      const data = await response.json();

      if (response.ok) {
        setSuccess("Message sent successfully!");
        setFormData({
          name: "",
          email: "",
          mobilenumber: "",
          message: "",
        });
      } else {
        setError(data.message || "Failed to send message.");
        if (response.status === 401) {
          localStorage.removeItem("token");
          setError("Session expired. Please log in again.");
        }
      }
    } catch (err) {
      setError("Network error. Please check if the server is running.");
      console.error(err);
    } finally {
      setLoading(false);
    }
  };

  return (
    <>
      <div className="min-h-screen bg-white">
        {/* Hero Section */}
        <section className="relative bg-gradient-to-br from-orange-50 to-yellow-50 py-16 overflow-hidden">
          <div className="max-w-7xl mx-auto px-6 grid md:grid-cols-2 gap-10 items-start">
            {/* Left – Text & Offices */}
            <div className="space-y-10">
              <div>
                <h1 className="text-4xl font-bold text-gray-900 mb-3">Get In Touch</h1>
                <p className="text-gray-700">
                  Want to hire a driver or drive for us? We'd love to hear from you! Here's how you can reach us.
                </p>
              </div>

              {/* Registered Office */}
              <div>
                <h3 className="font-semibold text-lg mb-2 flex items-center gap-2">
                  <MapPin className="w-5 h-5 text-orange-600" />
                  Registered Office
                </h3>
                <p className="text-gray-700 leading-relaxed">
                  Vardhaman Vihar Co-Op. Housing Society Ltd, 1/A/101,
                  Above Janata Bajar Vasai Rd.(W), Dist. Palghar - 401202
                </p>
                <div className="mt-3 space-y-1">
                  <p className="flex items-center gap-2 text-gray-800">
                    <Phone className="w-4 h-4" />
                    9066181808
                  </p>
                  <p className="flex items-center gap-2 text-gray-800">
                    <Mail className="w-4 h-4" />
                    saaviinfinet.helpdesk@gmail.com
                  </p>
                </div>
              </div>

              {/* Pune Office */}
              {/* <div>
                <h3 className="font-semibold text-lg mb-2">Pune Office</h3>
                <p className="text-gray-700 leading-relaxed">
                  114, Bhaskar Colony, Near Krome Mall, Next to Vijay Sales,
                  Solapur Road, Hadapsar, Pune 411013.
                </p>
                <p className="mt-2 flex items-center gap-2 text-gray-800">
                  <Phone className="w-4 h-4" />
                  +91 9625 390 390
                </p>
              </div> */}
            </div>

            {/* Right – Contact Form */}
            <div className="relative">
              {/* Decorative Yellow Circles */}
              <div className="absolute -top-10 -right-10 w-32 h-32 bg-yellow-200 rounded-full opacity-70"></div>
              <div className="absolute -bottom-16 -left-16 w-48 h-48 bg-yellow-100 rounded-full opacity-60"></div>
              <div className="absolute top-1/2 -translate-y-1/2 right-0 w-24 h-24 bg-yellow-300 rounded-full opacity-50"></div>

              <div className="relative bg-white p-8 rounded-xl shadow-lg border border-gray-100">
                <div className="bg-yellow-100 text-yellow-800 px-4 py-2 rounded-full inline-block mb-5 text-sm font-medium">
                  Are you a driver? Enquire Here!
                </div>

                {success && (
                  <div className="mb-4 p-3 bg-green-100 text-green-700 rounded-md text-sm">
                    {success}
                  </div>
                )}
                {error && (
                  <div className="mb-4 p-3 bg-red-100 text-red-700 rounded-md text-sm">
                    {error}
                  </div>
                )}

                <form onSubmit={handleSubmit} className="space-y-4">
                  <div className="grid grid-cols-2 gap-4">
                    <input
                      type="text"
                      name="name"
                      placeholder="Name *"
                      value={formData.name}
                      onChange={handleChange}
                      className="px-4 py-3 border border-gray-300 rounded-lg focus:outline-none focus:ring-2 focus:ring-orange-500"
                      required
                    />
                    <input
                      type="tel"
                      name="mobilenumber"
                      placeholder="Mobile Number *"
                      value={formData.mobilenumber}
                      onChange={handleChange}
                      className="px-4 py-3 border border-gray-300 rounded-lg focus:outline-none focus:ring-2 focus:ring-orange-500"
                      required
                    />
                  </div>

                  <input
                    type="email"
                    name="email"
                    placeholder="Email *"
                    value={formData.email}
                    onChange={handleChange}
                    className="w-full px-4 py-3 border border-gray-300 rounded-lg focus:outline-none focus:ring-2 focus:ring-orange-500"
                    required
                  />

                  <textarea
                    name="message"
                    rows={4}
                    placeholder="Message *"
                    value={formData.message}
                    onChange={handleChange}
                    className="w-full px-4 py-3 border border-gray-300 rounded-lg focus:outline-none focus:ring-2 focus:ring-orange-500 resize-none"
                    required
                  ></textarea>

                  <button
                    type="submit"
                    disabled={loading}
                    className={`w-full font-semibold py-3 rounded-lg transition ${
                      loading
                        ? "bg-gray-400 text-gray-700 cursor-not-allowed"
                        : "bg-gray-900 text-white hover:bg-gray-800"
                    }`}
                  >
                    {loading ? "Sending..." : "SEND"}
                  </button>
                </form>
              </div>
            </div>
          </div>
        </section>

        {/* Branches Section */}
        <section className="py-12 bg-gray-50">
          <div className="max-w-7xl mx-auto px-6">
            {/* <h3 className="font-semibold text-lg mb-6 flex items-center gap-2">
              <MapPin className="w-5 h-5 text-orange-600" />
              (Branches) Andheri
            </h3> */}
            <div className="grid md:grid-cols-3 gap-6 text-sm">
              {/* <div>
                <p className="font-medium">9/B Arasa shopping center,</p>
                <p>S.V road, Next to Paneri Shop,</p>
                <p>Andheri West, Mumbai - 400058.</p>
              </div> */}
              <div className="flex items-center gap-2 text-gray-800">
                <Mail className="w-4 h-4" />
               saaviinfinet@gmail.com
              </div>
              <div className="flex items-center gap-2 text-gray-800">
                <Phone className="w-4 h-4" />
                9066181808
              </div>
            </div>
          </div>
        </section>

        {/* MAP SECTION */}
        <section className="py-12 bg-white">
          <div className="max-w-7xl mx-auto px-6">
            <h2 className="text-2xl font-bold text-gray-900 mb-6 text-center">
              Find Us on Map
            </h2>

            <div className="relative w-full h-96 md:h-[500px] rounded-xl overflow-hidden shadow-xl border border-gray-200">
              <iframe
                title="Saavi Infinet Office Location"
                src="https://www.google.com/maps/embed?pb=!1m18!1m12!1m3!1d3770.785595123456!2d72.8317117!3d19.225751!2m3!1f0!2f0!3f0!3m2!1i1024!2i768!4f13.1!3m3!1m2!1s0x3be7b6f4b6f4b6f5%3A0x9f4b6f4b6f4b6f4b!2s19%C2%B022%2757.5%22N+72%C2%B049%2752.2%22E!5e0!3m2!1sen!2sin!4v1731312000000"
                width="100%"
                height="100%"
                style={{ border: 0 }}
                allowFullScreen=""
                loading="lazy"
                referrerPolicy="no-referrer-when-downgrade"
                className="absolute inset-0"
              ></iframe>
            </div>

            <div className="mt-6 text-center">
              <p className="text-sm text-gray-600">
                Coordinates: <strong>19°22'57.5"N 72°49'52.2"E</strong> | Near Vartak College Ground, Vasai West
              </p>
            </div>
          </div>
        </section>
      </div>
    </>
  );
};

export default CompanyContactUs;


